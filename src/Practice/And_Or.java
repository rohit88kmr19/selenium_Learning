package Practice;

public class And_Or {
    public static void main(String[] args) {
        System.out.println(12>10 && 5<10);
        System.out.println(12>=10 && 5<=10);
        System.out.println(12>15 && 9<10);

        System.out.println("-------------------------------");

        System.out.println(20>15 || 15<10);
        System.out.println(12>=10 || 15<=10);
        System.out.println(12>15 || 19<10);
    }
}
